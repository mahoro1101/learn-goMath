package elementaryMath

func Add(a int, b int) int {
	return a + b
}

// add 더하다

func Sub(a int, b int) int {
	return a - b
}

// substract 빼다

func Multi(a int, b int) int {
	return a * b
}

// multiply 곱하다

func Div(a int, b int) int {
	return a / b
}

// divide 나누다

func Mod(a int, b int) int {
	return a % b
}

// modulo 나머지 연산하다
